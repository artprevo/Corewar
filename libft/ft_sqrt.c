int	ft_sqrt(int nb)
{
	int n;

	n = 1;
	if (nb == 1)
		return (1);
	if (nb >= 2147483647)
		return (0);
	while (n < nb)
	{
		n++;
		if (n * n == nb)
			return (n);
	}
	return (0);
}
