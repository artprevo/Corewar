int	ft_is_prime(int nb)
{
	int n;

	n = 3;
	if (nb <= 1)
		return (0);
	if (nb % 2 == 0)
		return (nb == 2);
	while (n < nb)
	{
		if (nb % n == 0)
			return (0);
		n += 2;
	}
	return (1);
}
